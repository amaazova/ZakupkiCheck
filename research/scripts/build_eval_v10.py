"""W0 quality filter: take frozen v9 eval_dataset.csv → eval_dataset_v10.csv."""
from __future__ import annotations

import re
import sys
from pathlib import Path

import pandas as pd

# Allow either import style (module or direct script)
if __package__ in (None, ""):
    sys.path.insert(0, str(Path(__file__).resolve().parents[2]))
    from workspace.scripts.config import PROJECT_ROOT  # type: ignore
else:
    from .config import PROJECT_ROOT

V9_EVAL = PROJECT_ROOT / "workspace" / "eval" / "eval_dataset.csv"
V10_EVAL = PROJECT_ROOT / "workspace" / "eval" / "eval_dataset_v10.csv"
REPORT = PROJECT_ROOT / "workspace" / "eval" / "quality_filter_report.md"

NOT_TZ_PROBE_CHARS = 500
TZ_PHRASES = (
    re.compile(r"описание\s+объекта", re.IGNORECASE),
    re.compile(r"техническое\s+задание", re.IGNORECASE),
    re.compile(r"характеристик", re.IGNORECASE),
)


def _resolve_tz_path(rel: str | Path, notice_id: str) -> Path:
    """Build absolute path. Fallbacks: relative-to-PROJECT_ROOT → notice-id default."""
    p = Path(rel)
    if not p.is_absolute():
        p = PROJECT_ROOT / p
    if not p.is_file():
        # Defensive fallback: canonical layout is data/parsed_clean/{notice_id}/tz.md
        p = PROJECT_ROOT / "data" / "parsed_clean" / str(notice_id) / "tz.md"
    return p


def detect_not_tz(tz_path: Path) -> tuple[bool, bool]:
    """Returns (file_missing, not_tz).

    not_tz is only meaningful when file_missing=False.
    """
    if not tz_path.is_file():
        return True, False
    try:
        head = tz_path.read_text(encoding="utf-8", errors="replace")[:NOT_TZ_PROBE_CHARS]
    except OSError:
        return True, False
    not_tz = not any(rx.search(head) for rx in TZ_PHRASES)
    return False, not_tz


def assign_tier(char_count: int, not_tz: bool, file_missing: bool) -> tuple[str, str]:
    """Returns (quality_tier, drop_reason). drop_reason is '' for non-drop tiers."""
    if file_missing:
        return "drop", "file_missing"
    if not_tz:
        return "drop", "not_tz"
    if char_count < 1000:
        return "drop", "too_short"
    if char_count < 3000:
        return "thin", ""
    if char_count <= 200_000:
        return "normal", ""
    return "chunking_risk", ""


def build() -> dict:
    # notice_id MUST be string — pandas would otherwise read it as uint64 and
    # silently drop the leading zero (breaks every downstream path lookup).
    df = pd.read_csv(V9_EVAL, dtype={"notice_id": str})
    if "stratum" not in df.columns:
        raise RuntimeError("v9 eval_dataset.csv is missing 'stratum' column")

    # 1) Apply not_tz / size filter
    quality_tiers: list[str] = []
    drop_reasons: list[str] = []
    not_tz_flags: list[bool] = []
    file_missing_flags: list[bool] = []
    for _, row in df.iterrows():
        tz_abs = _resolve_tz_path(row["tz_path"], str(row["notice_id"]))
        missing, ntz = detect_not_tz(tz_abs)
        tier, reason = assign_tier(int(row.get("tz_char_count") or 0), ntz, missing)
        quality_tiers.append(tier)
        drop_reasons.append(reason)
        not_tz_flags.append(ntz)
        file_missing_flags.append(missing)

    df["not_tz"] = not_tz_flags
    df["file_missing"] = file_missing_flags
    df["drop_reason"] = drop_reasons
    df["quality_tier"] = quality_tiers

    # 2) c_16 stratum override (was service_only in v9, conditional in v10.2)
    c16_mask = df["cluster_id"] == 16
    c16_before_count = int(c16_mask.sum())
    c16_before_strata = df.loc[c16_mask, "stratum"].value_counts().to_dict()
    df.loc[c16_mask, "stratum"] = "conditional"

    # 3) Filter out drops
    pre_total = len(df)
    kept = df[df["quality_tier"] != "drop"].copy()
    drops = df[df["quality_tier"] == "drop"].copy()
    drop_breakdown = drops["drop_reason"].value_counts().to_dict()

    # 4) Final v10 columns (preserve v9 schema + quality_tier; not_tz kept for transparency)
    output_cols = [
        "episode_id", "notice_id", "cluster_id", "cluster_name",
        "fas_verdict", "stratum", "tz_path", "tz_char_count",
        "quality_tier",
    ]
    # add not_tz at the tail for diagnostic transparency (task spec mentions both columns)
    if "not_tz" not in output_cols:
        output_cols.append("not_tz")
    kept = kept[output_cols]
    V10_EVAL.parent.mkdir(parents=True, exist_ok=True)
    kept.to_csv(V10_EVAL, index=False)

    # 5) Report
    lines: list[str] = []
    lines += [
        "# Quality filter report — eval_dataset_v10",
        "",
        f"_Generated by `workspace/scripts/build_eval_v10.py` from frozen v9 eval._",
        "",
        "## Pipeline",
        "",
        f"- input: `{V9_EVAL.relative_to(PROJECT_ROOT)}`  ({pre_total} rows)",
        f"- output: `{V10_EVAL.relative_to(PROJECT_ROOT)}`  ({len(kept)} rows)",
        f"- dropped: **{len(drops)}**",
        "",
        "## Drop breakdown",
        "",
        "| reason | count |",
        "|---|--:|",
    ]
    for k in ("file_missing", "not_tz", "too_short"):
        lines.append(f"| {k} | {drop_breakdown.get(k, 0)} |")
    lines += [
        "",
        "## Quality tier distribution (kept rows)",
        "",
        "| quality_tier | count |",
        "|---|--:|",
    ]
    tier_counts = kept["quality_tier"].value_counts().reindex(
        ["thin", "normal", "chunking_risk"], fill_value=0
    )
    for t, n in tier_counts.items():
        lines.append(f"| {t} | {n} |")
    lines += [
        "",
        "## Verdict balance (kept rows)",
        "",
        "| fas_verdict | count |",
        "|---|--:|",
    ]
    for v, n in kept["fas_verdict"].value_counts().items():
        lines.append(f"| {v} | {n} |")
    lines += [
        "",
        "## Stratum distribution (kept rows, AFTER c_16 override)",
        "",
        "| stratum | count |",
        "|---|--:|",
    ]
    for s, n in kept["stratum"].value_counts().items():
        lines.append(f"| {s} | {n} |")

    # c_16 audit
    c16_after = kept[kept["cluster_id"] == 16]
    c16_after_strata = c16_after["stratum"].value_counts().to_dict()
    lines += [
        "",
        "## c_16 stratum override",
        "",
        f"- v9 cluster_id=16 rows: **{c16_before_count}** (strata before: `{c16_before_strata}`)",
        f"- after override + drop filter: **{len(c16_after)}** rows, strata = `{c16_after_strata}`",
        "",
    ]

    # Cross-tab tier × stratum × verdict for the kept set (compact diagnostic)
    lines += [
        "## Tier × stratum (kept rows)",
        "",
        "| tier | benchmarkable | conditional | service_only | other |",
        "|---|--:|--:|--:|--:|",
    ]
    for t in ("thin", "normal", "chunking_risk"):
        sub = kept[kept["quality_tier"] == t]
        b = int((sub["stratum"] == "benchmarkable").sum())
        c = int((sub["stratum"] == "conditional").sum())
        s = int((sub["stratum"] == "service_only").sum())
        o = len(sub) - b - c - s
        lines.append(f"| {t} | {b} | {c} | {s} | {o} |")
    lines.append("")

    REPORT.write_text("\n".join(lines), encoding="utf-8")

    return {
        "input_rows": pre_total,
        "kept_rows": len(kept),
        "drop_breakdown": drop_breakdown,
        "tier_counts": {t: int(n) for t, n in tier_counts.items()},
        "verdict_counts": {v: int(n) for v, n in kept["fas_verdict"].value_counts().items()},
        "stratum_counts": {s: int(n) for s, n in kept["stratum"].value_counts().items()},
        "c16_after": {"count": int(len(c16_after)), "strata": c16_after_strata},
        "out_csv": str(V10_EVAL),
        "out_report": str(REPORT),
    }


if __name__ == "__main__":
    summary = build()
    print(summary)
